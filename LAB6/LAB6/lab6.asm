	ORIG X3000
	LD R0, DAY	;THIS LOADS THE DAY INTO R0
	STI R0, DY	;THIS STORES THE DAY INTO X3100
	LD R0, MONTH	;THIS LOADS THE MONTH INTO R0
	STI R0, MT	;THIS STORES THE MONTH INTO X3101
	LD R0, YEAR	;THIS LOADS THE YEAR INTO R0
	LD R1, MONTH	;LOADS MONTH TO R1
	ADD R1, R1, #-2	;MONTH -2 TO CHECK TO SEE IF YEAR IS JANUARY OR FEBUARY.	
	BRp YC	; CHECK IF MONTH <2
	ADD R0, R0, #-1	;IF MONTH IS JANUARY OR FEBUARY -1 FROM YEAR
YC	STI R0, YR	;THIS STORES THE YEAR INTO X3102
	ST R0, YEAR 	;LOADS -100 INTO R1
	AND R0, R0, #0	;CLEARS R0 
	AND R1, R1, #0	;CLEARS R1 
 	AND R2, R2, #0	;CLEARS R2
	LD R0, YEAR	;LOADS YEAR INTO R0
	LD R1, NEG	;LOADS -100
	JSR DIVD	;JUMPS TO CALCULARE DECADE AND CENTURY
	STI R0, D	;STORES DECADE 	
	STI R2, C	;STORE CENTURY 
	AND R0, R0, #0	;CLEARS R0 
	AND R1, R1, #0	;CLEARS R1 
 	AND R2, R2, #0	;CLEARS R2
	LD R0, MONTH	;LOADS MONTH TO R0;
	ADD R0, R0, #15	;ADDS 15 TO R0
	ADD R0, R0, #6	;ADDS 6 MORE TO EQUAL 21	
	ADD R1, R1, #-12	;FILL R1 WITH -12
	JSR DIVD	;MONTH +21%12
	ADD R0, R0, #1	;MONTH +21%12+1
	STI R0, M	;Stores modified month
	AND R0, R0, #0	;CLEARS R0 
	AND R1, R1, #0	;CLEARS R1 
 	AND R2, R2, #0	;CLEARS R2
	LD R6, DAY	;ADDS DAY TO L6 WHICH WILL BE THE REGISTER FOR TO STORE CLACLULATION
	LDI R0, M	;LOAD MODIFIDE MONTH INTO R0
	;ADD R0, R0 ,#1
	ADD R1, R1, 13;	;ADD 13 TO R1
	JSR MULTI
	ADD R2,R2, #-1	;(13M)-1;;;;;;;;;;;;;;;;;;;;;;;;;;;;	
	ADD R0, R2, #0	;LOADS R2 TO R0;
	AND R1, R1, #0	;CLEARS R1
	AND R2, R2, #0 
	ADD R1, R1, #-5	;SETS R1 TO DIVEDE RO	
	JSR DIVD
	ADD R6, R6, R2	;ADDS VALUE OF DAY+(13M-1)/5
	LDI R0, D	;LOADS DECADE
	ADD R6,R6,R0	;ADDS VALUE OF (DAY+(13M-1)/5)+DECADE
	AND R0, R0, #0	;CLEARS R0 
	AND R1, R1, #0	;CLEARS R1 
	AND R2, R2, #0	;CLEARS R2
	LDI R0,D	;LOADS DECADE
	ADD R1,R1,#-4	;ADDS DIVISOR 
	JSR DIVD	
	ADD R6, R6, R2	;ADDS VALUE OF ((DAY+(13M-1)/5)+DECADE)+DECADE/4
	AND R0, R0, #0	;CLEARS R0 
	AND R1, R1, #0	;CLEARS R1 
 	AND R2, R2, #0	;CLEARS R2
	LDI R0, C	;LOADS DECADE
	ADD R1,R1,#-4	;ADDS DIVISOR 
	JSR DIVD	
	ADD R6, R6, R2	;ADDS VALUE OF (((DAY+(13M-1)/5)+DECADE)+DECADE/4)+CENTUARY/5
	AND R0, R0, #0	;CLEARS R0 
	AND R1, R1, #0	;CLEARS R1 
	AND R2, R2, #0	;CLEARS R2
	;LD R6, DAY	;ADDS DAY TO L6 WHICH WILL BE THE REGISTER FOR TO STORE CLACLULATION
	LDI R0, C	;LOAD MODIFIDE MONTH INTO R0
	ADD R1, R1, #2	;ADD 2 TO MULTIPLY CENTURY
	JSR MULTI
	NOT R2,R2
	ADD R2, R2, b1
	ADD R6, R6, R2	;ADDS VALUE OF ((((DAY+(13M-1)/5)+DECADE)+DECADE/4)+CENTUARY/5)-2*CENTUARY
	BRzp DONE
POS	ADD R6, R6 #7
	BRn POS	
DONE	AND R0, R0, #0	;CLEARS R0 
	AND R1, R1, #0	;CLEARS R1 
 	AND R2, R2, #0	;CLEARS R2
	ADD R0, R6, #0	;LOADS R6 INTO R0
	AND R6, R6, #0
	ADD R1, R1, #-7
	JSR DIVD
	ADD R6, R6, R0
	STI R6, D;	
	LEA R0, DAYS
	ADD R6, R6 #-0
LOOP	BRz DISPLAY
	ADD R0, R0, #10
	ADD R6, R6, #-1
	BR LOOP
DISPLAY	PUTS	

HALT

DAY	.FILL #14
MONTH 	.FILL #5
YEAR	.FILL #2014
DY	.FILL X31F1
MT	.FILL X31F0
YR	.FILL X31F2
D	.FILL X31F3
C	.FILL X3104 
M	.FILL X3105
NEG	.FILL #-100
DAYS 	
.STRINGZ "Sunday   "
.STRINGZ "Monday   "
.STRINGZ "Tuesday  "
.STRINGZ "Wednesday"
.STRINGZ "Thursday "
.STRINGZ "Friday   "
.STRINGZ "Saterday "

DIVD	ADD R2, R2, #0	
MINUS	ADD R2, R2, #1	;START TO FIND CENTURY
ADD R0, R0, R1	; YEAR -100
BRp MINUS
ADD R0, R0, #0
BRz J	;YEAR ENDS IN 00 JUMPS TO STORE
ADD R2, R2, #-1	;REDUCES CENTURY BY 1
NOT R1, R1	;SWICHS R1 TO POSTIVE
ADD R1, R1, b1	
ADD R0, R0, R1	;CONVERTS DECADE BACK TO POSITIVE
J	RET

MULTI	ADD R0, R0, #-1	
ADD R2, R2, R1
PLM	ADD R2, R2, R1
ADD R0, R0, #-1
BRp PLM
RET

