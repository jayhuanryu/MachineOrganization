.ORIG X3000
	AND R3, R3, #0	;CLEARS R3
	AND R4, R4, #0  ;CLEARS R4
	AND R2, R2, #0	;CLEARS R2
	AND R1, R1, #0	;CLEARS R1
	ADD R3, R3, #1	;ADDS 1 TO R3 
	ADD R4, R4, #1	;ADDS 1 TO R4
 	LDI R7, FNP	;LOADS FIBINACHI PLACE INTO R7
	ADD R7, R7, #-2 ;MINUSES 2 TO REMOVE R3 AND R4 = 1
	BRnz PLACE	;CHECKS THE LOOP TO SEE IF IT IS NEGATIVE OR POSITIVE
LOOP	ADD R5, R4, R3	;ADDS R3 AND R4 TOGETHER TO FIND THE NEXT FIBANOCHIE NUMBER;
	ADD R3, R4, #0 	;PLACES R4 IN R3
	ADD R4, R5, #0  ;PLACE R5 IN R4
	ADD R7, R7, #-1 ;DECREMENTS THE LOOP
	BRp LOOP	;IF R7 POSITIVE BEGINS THE LOOP AGAIN
PLACE	STI R4, FBN ;AFTER LOOP IS OVER STORS VALUE IN ADRESSx3101
	AND R3, R3, #0	;CLEARS R3
	AND R4, R4, #0	;CLEARS R4
	AND R2, R2, #0	;CLEARS R2
	AND R7, R7, #0 	;CLEARS R7
	ADD R3, R2, #1	;ADDS 1 TO R3
	ADD R4, R4, #1	;ADDS 1 TO R4
	ADD R7, R7, #2	;ADDS 2 TO HOLD THE CORRECT PLACE IN THE SEQUENCE
	ADD R5, R4, R3	;ADDS R3 AND R4 TOGETHER TO FIND THE NEXT FIBANOCHIE NUMBER;
LOOP2	ADD R3, R4, #0 	;PLACES R4 IN R3
	ADD R4, R5, #0  ;PLACE R5 IN R4
	ADD R7, R7, #1  ;INCREMENTS THE LOOP
	ADD R5, R4, R3	;ADDS R3 AND R4 TOGETHER TO FIND THE NEXT FIBANOCHIE NUMBER;
	BRp LOOP2
	STI R4, FLP
	STI R7, LFP
	HALT
FNP	.FILL X3100
FBN	.FILL X3101
FLP	.FILL X3102
LFP	.FILL X3103	
